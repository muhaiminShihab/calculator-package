<?php

if (!function_exists('plus')) {

    function plus($a, $b)
    { 
        return $a + $b; 
    }
}

if (!function_exists('minus')) {

    function minus($a, $b)
    { 
        return $a - $b; 
    }
}

if (!function_exists('into')) {

    function into($a, $b)
    { 
        return $a * $b; 
    }
}

if (!function_exists('devide')) {

    function devide($a, $b)
    { 
        return $a / $b; 
    }
}